export const I18nProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const { sessionId, isAuthenticated } = useAuth();
  const [language, setLanguageState] = useState<Language>('fr'); // Default to French
  const [isLoading, setIsLoading] = useState(true);

  // Load language preference from backend on mount and when auth changes
  useEffect(() => {
    const loadLanguage = async () => {
      try {
        if (isAuthenticated) {
          // Use the same call as your Settings component
          const response = await invoke<LanguageResponse>('get_user_language');
          setLanguageState(response.language as Language);
        } else {
          // If not authenticated, use default language (French)
          setLanguageState('fr');
        }
      } catch (error) {
        console.error('Failed to load language preference:', error);
        setLanguageState('fr'); // Default to French on error
      } finally {
        setIsLoading(false);
      }
    };

    loadLanguage();
  }, [isAuthenticated, sessionId]);

  // Save language preference to backend
  const setLanguage = async (lang: Language): Promise<boolean> => {
    if (!sessionId) {
      console.error('No active session for language update');
      return false;
    }

    try {
      // Use the same call as your Settings component
      const response = await invoke<UpdateResponse>('update_user_language', {
        language: lang,
        sessionId
      });

      if (response.success) {
        setLanguageState(lang);
        return true;
      } else {
        console.error('Failed to update language:', response.message);
        return false;
      }
    } catch (error) {
      console.error('Failed to save language preference:', error);
      return false;
    }
  };

  // Translation function with parameter support
  const t = (key: string, params?: Record<string, string | number>): string => {
    const translation = translations[language]?.[key] || key;

    if (!params) {
      return translation;
    }

    // Replace parameters in the translation
    return Object.entries(params).reduce((text, [paramKey, paramValue]) => {
      return text.replace(new RegExp(`{{${paramKey}}}`, 'g'), String(paramValue));
    }, translation);
  };

  const value: I18nContextType = {
    language,
    setLanguage,
    t,
    isLoading
  };

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
};